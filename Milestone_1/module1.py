import requests
import pandas as pd
from bs4 import BeautifulSoup
from functools import reduce
from metric_links import other_sources

HEADERS = {
    "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
                  "AppleWebKit/537.36 (KHTML, like Gecko) "
                  "Chrome/120.0.0.0 Safari/537.36"
}

def scrape_metric(url, metric_name):
    response = requests.get(url, headers=HEADERS, timeout=20)
    soup = BeautifulSoup(response.text, "html.parser")

    rows = soup.select(".topRow")
    data = []

    for row in rows:
        texts = [t.strip() for t in row.stripped_strings]
        if len(texts) >= 3:
            data.append({
                "country": texts[1],
                metric_name: texts[-1]
            })

    return pd.DataFrame(data)

dfs = []

for url, metric in other_sources.items():
    print("Scraping:", metric)
    df_metric = scrape_metric(url, metric)
    dfs.append(df_metric)

final_df = reduce(lambda l, r: pd.merge(l, r, on="country", how="outer"), dfs)

final_df.to_csv("military_raw_data.csv", index=False)

print("FINAL SHAPE:", final_df.shape)


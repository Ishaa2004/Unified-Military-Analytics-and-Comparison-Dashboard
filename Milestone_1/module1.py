import requests
from bs4 import BeautifulSoup

BASE_URL = "https://www.globalfirepower.com"
LISTING_URL = f"{BASE_URL}/countries-listing.php"

response = requests.get(LISTING_URL)
soup = BeautifulSoup(response.text, "html.parser")

country_links = []

for a in soup.select("a[href*='country-military-strength-detail.php']"):
    full_url = BASE_URL + "/" + a["href"].lstrip("/")
    country_links.append(full_url)

country_links = list(set(country_links))

with open("country_urls.txt", "w") as f:
    for link in country_links:
        f.write(link + "\n")

print("Total country URLs found:", len(country_links))

import requests
import pandas as pd
from bs4 import BeautifulSoup
import time

with open("country_urls.txt") as f:
    urls = [u.strip() for u in f if u.strip()]

all_data = []

for url in urls:
    print("Scraping:", url)

    try:
        soup = BeautifulSoup(requests.get(url, timeout=15).text, "html.parser")
        data = {}

        # Country name
        h1 = soup.find("h1")
        data["country"] = h1.text.strip() if h1 else "Unknown"

        # Rank & Power Index
        for div in soup.select(".pwrIndx"):
            txt = div.text.lower()
            val = div.find("span").text.strip()
            if "rank" in txt:
                data["rank"] = val
            if "power index" in txt:
                data["power_index"] = val

        # ALL metric rows
        for row in soup.select("li"):
            label = row.find("span", class_="text")
            value = row.find("span", class_="value")

            if label and value:
                key = label.text.strip().lower().replace(" ", "_")
                data[key] = value.text.strip()

        all_data.append(data)
        time.sleep(1)

    except Exception as e:
        print("Failed:", e)

df = pd.DataFrame(all_data)
df.to_csv("military_raw_data.csv", index=False)

print("Countries scraped:", df.shape[0])


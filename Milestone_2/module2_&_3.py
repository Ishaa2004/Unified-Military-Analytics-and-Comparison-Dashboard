import pandas as pd
from sklearn.preprocessing import MinMaxScaler

# =========================
# 1. Load Cleaned Data
# =========================
file_path = "military_cleaned_data_step1.csv"
df = pd.read_csv(file_path)

# =========================
# 2. Select Core Power Features
# =========================
power_features = {
    # Manpower
    "active_personnel": 0.15,
    "reserve_personnel": 0.05,
    "paramilitary_forces": 0.05,

    # Air Power
    "total_military_aircraft": 0.12,
    "fighter_aircraft": 0.08,
    "attack_aircraft": 0.05,

    # Land Power
    "tanks": 0.10,
    "artillery": 0.08,

    # Naval Power
    "total_naval_assets": 0.07,
    "submarines": 0.05,

    # Economy / Budget
    "defense_budget_usd": 0.15
}

features = list(power_features.keys())
weights = list(power_features.values())

# =========================
# 3. Handle Missing Columns (Safety)
# =========================
for col in features:
    if col not in df.columns:
        df[col] = 0

# =========================
# 4. Normalize Features
# =========================
scaler = MinMaxScaler()
df_scaled = df.copy()
df_scaled[features] = scaler.fit_transform(df[features])

# =========================
# 5. Compute Power Index Score
# =========================
df_scaled["power_index_score"] = 0

for col, w in power_features.items():
    df_scaled["power_index_score"] += df_scaled[col] * w

# =========================
# 6. Create Power Index Rank
# =========================
df_scaled["power_index_rank"] = df_scaled["power_index_score"].rank(
    ascending=False,
    method="dense"
).astype(int)

# =========================
# 7. Sort by Rank
# =========================
df_power_index = df_scaled.sort_values("power_index_rank")

# =========================
# 8. Save Output
# =========================
output_file = "Military_Power_Index.csv"
df_power_index.to_csv(output_file, index=False)

print("Power Index file created:", output_file)

# Preview top 10
print(df_power_index[["country", "power_index_score", "power_index_rank"]].head(10))


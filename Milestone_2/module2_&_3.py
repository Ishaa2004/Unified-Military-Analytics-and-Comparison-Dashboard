import pandas as pd
import re

# Load the file
df = pd.read_csv('military_raw_data (2).csv')

def clean_value(val):
    if pd.isna(val):
        return 0
    # Convert to string to handle any mixed types
    val = str(val)
    # 1. Remove newlines, tabs, and carriage returns
    val = re.sub(r'[\r\n\t]', '', val)
    # 2. Remove non-numeric characters (keep digits and decimal points)
    # This strips things like "$", "km", "bbl", "Cu.M", "mt", and ","
    clean_val = re.sub(r'[^\d.]', '', val)

    # 3. Convert to numeric, return 0 if empty after cleaning
    try:
        return pd.to_numeric(clean_val)
    except:
        return 0

print("Cleaning function ready.")

